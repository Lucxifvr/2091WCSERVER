var calc = {
  add: function(a, b) {
    let y = a + b;
    return y;
  },
  
  subtract: function(a, b) {
    let y = a - b;
    return y;
  },
  
  multiply: function(a, b) {
    let y = a * b;
    return y;
  },
  
  divide: function(a, b) {
    let y = a / b;
    return y;
  }

}

module.exports = calc;